package com.example.tutorup;

public class Student {
    int id;
    String name;
    String email;
    String pass;
    String edu;
    float balance;

    public Student(int id, String name, String email, String pass, String edu, float balance){
        this.id = id;
        this.name = name;
        this.email = email;
        this.pass = pass;
        this.edu = edu;
        this.balance = balance;
    }
}
